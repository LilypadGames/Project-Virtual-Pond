// Color Scheme

class ColorScheme {
    get White() {
        return this.constructor.White;
    }
    get LightGray() {
        return this.constructor.LightGray;
    }
    get Black() {
        return this.constructor.Black;
    }
    get LightBlue() {
        return this.constructor.LightBlue;
    }
    get Blue() {
        return this.constructor.Blue;
    }
    get DarkBlue() {
        return this.constructor.DarkBlue;
    }
    get DarkerBlue() {
        return this.constructor.DarkerBlue;
    }
    get Red() {
        return this.constructor.Red;
    }
    get DarkRed() {
        return this.constructor.DarkRed;
    }
    get Gold() {
        return this.constructor.Gold;
    }
}

